// State name normalization for matching with data
export const normalizeStateName = (name: string): string => {
  const normalized = name.toLowerCase().trim();
  const mappings: Record<string, string> = {
    'andhra pradesh': 'andhra-pradesh',
    'arunachal pradesh': 'arunachal-pradesh',
    'himachal pradesh': 'himachal-pradesh',
    'madhya pradesh': 'madhya-pradesh',
    'tamil nadu': 'tamil-nadu',
    'uttar pradesh': 'uttar-pradesh',
    'west bengal': 'west-bengal',
    'jammu and kashmir': 'jammu-kashmir',
    'jammu & kashmir': 'jammu-kashmir',
    'andaman and nicobar': 'andaman-nicobar',
    'andaman & nicobar': 'andaman-nicobar',
    'dadra and nagar haveli': 'dadra-nagar-haveli',
    'daman and diu': 'daman-diu',
  };
  return mappings[normalized] || normalized.replace(/\s+/g, '-');
};

export const getStateName = (id: string): string => {
  const state = INDIA_STATES.find(s => s.id === id);
  return state?.name || id;
};
